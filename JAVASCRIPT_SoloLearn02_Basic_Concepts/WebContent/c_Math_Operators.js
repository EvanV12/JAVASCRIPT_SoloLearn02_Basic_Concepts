// Increment & Decrement
// var++ Post Increment
	var a1 = 0, b1 = 10;
	var a1 = b1++;
	// a = 10 and b = 11;

// ++var Pre Increment
	var a2 = 0, b2 = 10;
	var a2 = ++b2;
	// a = 11 and b = 11

// var-- Post Decrement
	var a3 = 0, b3 = 10;
	var a3 = b3--;
	// a = 10 and b = 9
	
// --var Pre Decrement
	var a4 = 0, b4 = 10;
	var a4 = --b4;
	// a = 9 and b = 9